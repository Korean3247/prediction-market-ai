"""
SQLAlchemy ORM models for the prediction market AI system.
"""

import enum
from datetime import datetime
from typing import Any, Dict, List, Optional

from sqlalchemy import (
    Boolean,
    Column,
    DateTime,
    Enum,
    Float,
    ForeignKey,
    Integer,
    String,
    Text,
    JSON,
    func,
)
from sqlalchemy.orm import DeclarativeBase, relationship


class Base(DeclarativeBase):
    """Base class for all SQLAlchemy models."""
    pass


class ActionEnum(str, enum.Enum):
    """Possible risk decision actions."""
    BUY = "buy"
    SKIP = "skip"
    OBSERVE = "observe"


class MarketStatus(str, enum.Enum):
    """Market lifecycle status."""
    ACTIVE = "active"
    RESOLVED = "resolved"
    CANCELLED = "cancelled"


class Market(Base):
    """
    Represents a prediction market fetched from external platforms.
    """
    __tablename__ = "markets"

    id = Column(Integer, primary_key=True, index=True)
    market_id = Column(String(255), unique=True, nullable=False, index=True)
    title = Column(String(1000), nullable=False)
    description = Column(Text, nullable=True)
    category = Column(String(255), nullable=True)
    platform = Column(String(100), nullable=False)
    current_price = Column(Float, nullable=False)          # 0-1 probability
    liquidity = Column(Float, default=0.0)
    volume_24h = Column(Float, default=0.0)
    spread = Column(Float, default=0.0)
    resolve_time = Column(DateTime, nullable=True)
    priority_score = Column(Float, default=0.0)
    flags = Column(JSON, default=dict)                     # anomaly flags
    status = Column(
        Enum(MarketStatus),
        default=MarketStatus.ACTIVE,
        nullable=False,
    )
    url = Column(String(1000), nullable=True)
    created_at = Column(DateTime, default=func.now(), nullable=False)
    updated_at = Column(DateTime, default=func.now(), onupdate=func.now(), nullable=False)

    # Relationships
    research_reports = relationship(
        "ResearchReport", back_populates="market", cascade="all, delete-orphan"
    )
    predictions = relationship(
        "Prediction", back_populates="market", cascade="all, delete-orphan"
    )
    risk_decisions = relationship(
        "RiskDecision", back_populates="market", cascade="all, delete-orphan"
    )
    outcomes = relationship(
        "Outcome", back_populates="market", cascade="all, delete-orphan"
    )

    def __repr__(self) -> str:
        return (
            f"<Market(id={self.id}, market_id='{self.market_id}', "
            f"title='{self.title[:50]}', platform='{self.platform}')>"
        )


class ResearchReport(Base):
    """
    Research report generated for a specific market.
    Combines news sentiment, credibility analysis, and LLM summary.
    """
    __tablename__ = "research_reports"

    id = Column(Integer, primary_key=True, index=True)
    market_id = Column(
        Integer,
        ForeignKey("markets.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )
    keywords = Column(JSON, default=list)           # extracted search keywords
    summary = Column(Text, nullable=True)           # LLM-generated summary
    sentiment_score = Column(Float, default=0.0)   # -1 to 1
    source_count = Column(Integer, default=0)
    credibility_score = Column(Float, default=0.5) # 0 to 1
    raw_data = Column(JSON, default=dict)           # raw API responses
    created_at = Column(DateTime, default=func.now(), nullable=False)

    # Relationship
    market = relationship("Market", back_populates="research_reports")

    def __repr__(self) -> str:
        return (
            f"<ResearchReport(id={self.id}, market_id={self.market_id}, "
            f"sentiment={self.sentiment_score:.2f})>"
        )


class Prediction(Base):
    """
    Probability prediction for a market, generated by the prediction agent.
    """
    __tablename__ = "predictions"

    id = Column(Integer, primary_key=True, index=True)
    market_id = Column(
        Integer,
        ForeignKey("markets.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )
    predicted_probability = Column(Float, nullable=False)  # 0-1
    implied_probability = Column(Float, nullable=False)    # derived from market price
    edge = Column(Float, nullable=False)                   # predicted - implied
    confidence_score = Column(Float, nullable=False)       # 0-1
    model_version = Column(String(50), default="v1.0")
    reasoning = Column(Text, nullable=True)                # LLM explanation
    created_at = Column(DateTime, default=func.now(), nullable=False)

    # Relationship
    market = relationship("Market", back_populates="predictions")

    def __repr__(self) -> str:
        return (
            f"<Prediction(id={self.id}, market_id={self.market_id}, "
            f"predicted={self.predicted_probability:.3f}, "
            f"edge={self.edge:.3f})>"
        )


class RiskDecision(Base):
    """
    Risk management decision for a market position.
    """
    __tablename__ = "risk_decisions"

    id = Column(Integer, primary_key=True, index=True)
    market_id = Column(
        Integer,
        ForeignKey("markets.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )
    action = Column(Enum(ActionEnum), nullable=False)
    recommended_size = Column(Float, default=0.0)          # dollar amount
    ev = Column(Float, default=0.0)                        # expected value
    risk_score = Column(Float, default=0.5)                # 0-1, higher = riskier
    reason = Column(Text, nullable=True)
    price_at_decision = Column(Float, nullable=True)       # market price when decided
    created_at = Column(DateTime, default=func.now(), nullable=False)

    # Relationship
    market = relationship("Market", back_populates="risk_decisions")

    def __repr__(self) -> str:
        return (
            f"<RiskDecision(id={self.id}, market_id={self.market_id}, "
            f"action='{self.action}', size={self.recommended_size:.2f})>"
        )


class Outcome(Base):
    """
    Recorded outcome for a resolved market, including PnL and failure analysis.
    """
    __tablename__ = "outcomes"

    id = Column(Integer, primary_key=True, index=True)
    market_id = Column(
        Integer,
        ForeignKey("markets.id", ondelete="CASCADE"),
        nullable=False,
        index=True,
    )
    actual_result = Column(Boolean, nullable=False)   # True = YES resolved
    pnl = Column(Float, default=0.0)                  # profit/loss in USD
    review_notes = Column(Text, nullable=True)        # LLM review notes
    failure_tags = Column(JSON, default=list)         # e.g. ["prediction_error"]
    created_at = Column(DateTime, default=func.now(), nullable=False)

    # Relationship
    market = relationship("Market", back_populates="outcomes")

    def __repr__(self) -> str:
        return (
            f"<Outcome(id={self.id}, market_id={self.market_id}, "
            f"result={self.actual_result}, pnl={self.pnl:.2f})>"
        )


class ArbOpportunity(Base):
    """
    Cross-platform arbitrage opportunity: same market at different prices.
    Guaranteed profit = price_high - price_low (theoretical, before fees/slippage).
    """
    __tablename__ = "arb_opportunities"

    id = Column(Integer, primary_key=True, index=True)
    market_id_a = Column(Integer, ForeignKey("markets.id"), nullable=False)
    market_id_b = Column(Integer, ForeignKey("markets.id"), nullable=False)
    platform_a = Column(String(100), nullable=False)
    platform_b = Column(String(100), nullable=False)
    price_a = Column(Float, nullable=False)           # YES price on platform A
    price_b = Column(Float, nullable=False)           # YES price on platform B
    delta = Column(Float, nullable=False)             # abs(price_a - price_b)
    guaranteed_profit_pct = Column(Float, nullable=False)  # delta / (price_low + 1 - price_high)
    title_similarity = Column(Float, nullable=False)  # Jaccard similarity of titles
    status = Column(String(50), default="open")       # open / expired / closed
    created_at = Column(DateTime, default=func.now(), nullable=False)

    market_a = relationship("Market", foreign_keys=[market_id_a])
    market_b = relationship("Market", foreign_keys=[market_id_b])

    def __repr__(self) -> str:
        return (
            f"<ArbOpportunity(id={self.id}, {self.platform_a}↔{self.platform_b}, "
            f"delta={self.delta:.3f}, profit={self.guaranteed_profit_pct:.1%})>"
        )


class PaperTrade(Base):
    """
    Virtual paper trade for tracking AI signal performance without real money.
    """
    __tablename__ = "paper_trades"

    id = Column(Integer, primary_key=True, index=True)
    market_id = Column(Integer, ForeignKey("markets.id"), nullable=False)
    direction = Column(String, default="YES")          # YES or NO
    entry_price = Column(Float, nullable=False)        # market price at entry
    predicted_prob = Column(Float, nullable=False)
    edge = Column(Float, nullable=False)
    confidence = Column(Float, nullable=False)
    size_usd = Column(Float, default=10.0)             # virtual $10 per trade
    is_short_term = Column(Boolean, default=False)
    status = Column(String, default="open")            # open, won, lost
    actual_result = Column(Boolean, nullable=True)
    pnl = Column(Float, nullable=True)
    created_at = Column(DateTime, default=datetime.utcnow)
    resolved_at = Column(DateTime, nullable=True)

    # Relationship
    market = relationship("Market")

    def __repr__(self) -> str:
        return (
            f"<PaperTrade(id={self.id}, market_id={self.market_id}, "
            f"direction='{self.direction}', status='{self.status}', pnl={self.pnl})>"
        )
